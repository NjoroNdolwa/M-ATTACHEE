package com.environment;

public class UserClass {
    String name,school,programme,moduleYear,course,phone,email,y1,y2,y3,y4,y5;
    public UserClass() {
    }

    public UserClass(String name, String school, String programme, String moduleYear, String course, String phone, String email, String y1, String y2, String y3, String y4, String y5) {
        this.name = name;
        this.school = school;
        this.programme = programme;
        this.moduleYear = moduleYear;
        this.course = course;
        this.phone = phone;
        this.email = email;
        this.y1 = y1;
        this.y2 = y2;
        this.y3 = y3;
        this.y4 = y4;
        this.y5 = y5;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getSchool() {
        return school;
    }

    public void setSchool(String school) {
        this.school = school;
    }

    public String getProgramme() {
        return programme;
    }

    public void setProgramme(String programme) {
        this.programme = programme;
    }

    public String getModuleYear() {
        return moduleYear;
    }

    public void setModuleYear(String moduleYear) {
        this.moduleYear = moduleYear;
    }

    public String getCourse() {
        return course;
    }

    public void setCourse(String course) {
        this.course = course;
    }

    public String getPhone() {
        return phone;
    }

    public void setPhone(String phone) {
        this.phone = phone;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getY1() {
        return y1;
    }

    public void setY1(String y1) {
        this.y1 = y1;
    }

    public String getY2() {
        return y2;
    }

    public void setY2(String y2) {
        this.y2 = y2;
    }

    public String getY3() {
        return y3;
    }

    public void setY3(String y3) {
        this.y3 = y3;
    }

    public String getY4() {
        return y4;
    }

    public void setY4(String y4) {
        this.y4 = y4;
    }

    public String getY5() {
        return y5;
    }

    public void setY5(String y5) {
        this.y5 = y5;
    }
}

